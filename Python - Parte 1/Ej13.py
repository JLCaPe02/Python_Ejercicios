# Escriba un programa que lea dos números y lo visualiza en orden ascendente

num1 = int(input("Introduce número uno "))
num2 = int(input("Introduce número dos "))

if num1 > num2:
    print("El orden ascendente de los números es ", num2, num1)
else:
    print("El orden ascendente de los números es ", num1, num2)
